<p>
De 12 ori / an, timp de un weekend reunim o echipă multi-disciplinară (40-50 de medici specialiști,
rezidenți, studenți), în colaborare cu  autoritățile locale. Evaluăm gratuit starea de sănătate a
populației din zona respectivă prin: consult clinic, analize bioumorale, electrocardiogramă,   examene
ecografice, consultații de specialitate: medicină internă, cardiologie, neurologie, oftalmologie,
dermatologie, ginecologie, radiologie și imagistică medicală, pediatrie etc.
</p>

<p>
Proiectul nostru se adresează României rurale, oamenilor din zone care nu beneficiază nici măcar de
servicii medicale primare și nu au acces la sistemul sanitar din motive economice, infrastructură sau
pur și simplu pentru ca nu le permite starea fizică să se deplaseze la un spital.
</p>

<p>
Organizăm caravane desfășurate pe parcursul a 2 zile (sâmbătă și duminică), la care participă o
echipă multidisciplinară (studenți, medici specialiști și rezidenți), care consultă 100 – 200 persoane,
adulți și copii.
</p>

<p>
De asemenea, avem susținerea permanentă a Synevo, care ne însoțesc în fiecare caravană și
recoltează un pachet de analize sangvine pentru 200 oameni. Panelul de analize recoltate a fost
stabilit în colaborare cu noi, în ideea de a beneficia de toate informațiile necesare unei evaluări
complete a stării de sănătate.
</p>

<p>
Și nu în ultimul rând, avem susținerea a numeroase spitale din București prin intermediul medicilor
din cadrul acestora care ne însoțesc în activitatea noastră.
</p>

