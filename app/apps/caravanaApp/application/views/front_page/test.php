    <p>
        Asociatia <b>Caravana cu Medici</b> este fondată în 2014 de 5 medici rezidenti care au alături o echipă de
      aproape 250 de medici voluntari.  Misiunea ONG-ului este să realizeze o rețea națională prin
      intermediul căreia medicii să acorde periodic îngrijiri medicale persoanelor aflate în zonele mai puțin
      favorizate din punct de vedere economic. Proiectul se desfășoară sub egida Societății Române de
      Medicină Internă și în colaborare cu Clinica de Nefrologie și Medicină Internă a Institutului Clinic
      Fundeni.
    </p>

    <p>
      Programul asociației presupune realizarea unei serii de caravane medicale, reunind 40 de medici
      primari și rezidenți voluntari, care se deplasează, pe parcursul a două zile, în diverse zone rurale din
      țară pentru a acorda, în regim gratuit, consultații medicale de specialitate populației locale cu o
      situație financiară precară sau care nu beneficiază în mod frecvent de servicii medicale.
    </p>

    <p>
      Consultațiile acordate includ: recoltarea de sânge prin puncție venoasă (cu ajutorul unui laborator
      mobil), examen clinic complet cu electrocardiogramă, ecografie abdominală și examen oftalmologic,
      consult de specialitate cu recomandări și scrisoare medicală.
    </p>

    <p>
      În 2016 ne dorim să organizăm minim 12 caravana cu sprijinul partenerilor nostri de la Fundația
      Vodafone Romania, Synevo Romania, Ambasada Danemarcei și AVON Romania.
    </p>

